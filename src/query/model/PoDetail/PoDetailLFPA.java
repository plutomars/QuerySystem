	public String[] getValues(int type) {
		ArrayList<String> valueList = new ArrayList<String>();

		switch (type) {
			case 1:
				valueList.add(matured_date);
				valueList.add(expired_date);
				valueList.add(reminder_date1+" / "+reminder_date2);
				valueList.add(paid_to_date);
				valueList.add(bill_to_date);
				valueList.add(as_date);
				valueList.add(gap_start_date);
				valueList.add(pay_acct_no);
				valueList.add(group_code+" / "+group_and_auto);
//				valueList.add(group_and_auto);
				valueList.add(rider_conti);
				valueList.add(res_apply);
				valueList.add(fyp_ind);
				valueList.add(last_effect_date);					 	//前變更生效日
				valueList.add(last_process_date+" "+last_process_user);		//前變更日/前變更者
				break;
			case 2:
				valueList.add(amt_28250006+" / "+amt_28250007);
				valueList.add(amt_28250008+" / "+edp_susp);
				valueList.add(acct21+" / "+anrf_seq_no);
				valueList.add(div_desc);
				valueList.add(div_amt+" / "+div_decl);
				valueList.add(prem_susp);
				valueList.add(mode_prem_h+" / "+accu_prem);  //每期保費/應繳保費
				valueList.add(getdisc_amt() +" / "+disc_deadline); //折扣金額/折扣期限
				valueList.add(prem_deadline);
				valueList.add(post_date_y+" / "+post_date_n);
				valueList.add(last_check_date);
				valueList.add(mode_prem_h+" / "+prem_disc_perc_h);	//高保費/折扣
				valueList.add(prem_disc_perc+" "+group_ind);		//保費折扣＆集彙指示
				valueList.add(next_activity_date+" / "+next_activity_type);		//次活動日/活動型態
				break;
			case 3:
				valueList.add(loan_rate+" / "+pricing_ix);
				valueList.add(loan_date);
				valueList.add(loan_amt);
				valueList.add(nf_option_desc);
				valueList.add(apl_date);
				valueList.add(apl_amt);
				valueList.add(ol_rem_date);
				valueList.add(sign_paper_date);
				valueList.add(back_date);
				valueList.add(reprint_page_sw);
				valueList.add(mp_code+" / "+bank_name);
				valueList.add(mc_ident+" / "+mc_mail);
				valueList.add(popu_code+" / "+company_name);
//				valueList.add(next_activity_date+" / "+next_activity_type);		//次活動日/活動型態
				break;
		}
		String[] valueArray = new String[valueList.size()];
		valueList.toArray(valueArray);
		
		return valueArray;
	}

