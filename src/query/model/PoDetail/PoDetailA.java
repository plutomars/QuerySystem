	public String[] getValues(int type) {
		ArrayList<String> valueList = new ArrayList<String>();

		switch (type) {
		case 1:
			valueList.add(expired_date);
			valueList.add(paid_to_date);
			valueList.add(paid_to_date_coi);
			valueList.add(anpay_str_date);
			valueList.add(anpay_code);
			valueList.add(anpay_ind_desc);
			valueList.add(period_certain);
			valueList.add(init_decl_int_r);
			valueList.add(accu_decl_int_r);
			valueList.add(an_loan_int_rate);
			valueList.add(surrend_fee_r);			
			valueList.add(fyp_ind);
			valueList.add(bill_to_date);
			valueList.add(last_effect_date); // 前變更生效日
			valueList.add(acct21 + "/" + anrf_seq_no);
			break;
		case 2:
			valueList.add(policy_year);
			valueList.add(check_d);
			valueList.add(loan_d);
			valueList.add(allow_loan_amt);
			valueList.add(part_wd_free);
			valueList.add(maxi_part_wd);
			valueList.add(ol_date);
			valueList.add(amt_0008 + "/" + edp_susp);
			valueList.add(amt_0006 + "/" + amt_0007);
			valueList.add(mp_code + "/" + bank_name);
			valueList.add(prem_susp); // 101/10/17 新增溢欠繳
			valueList.add(fy_billing_desc);
			valueList.add(plan_prem);
			valueList.add(last_process_date + " " + last_process_user); // 前變更日/前變更者
			break;
		case 3:
			valueList.add(accu_date);
			valueList.add(accu_date_end);
			valueList.add(accu_int_balance);
			valueList.add(int_this_time);
			valueList.add(accumulated_amt);
			valueList.add(policy_value);
			valueList.add(loan_date);
			valueList.add(loan_date_end);
			valueList.add(loan_int_balance);
			valueList.add(loan_int_this_t);
			valueList.add(loan_amt);
			valueList.add(loan_total);
			valueList.add(mc_ident + "/" + mc_mail);
			valueList.add(next_activity_date + " / " + next_activity_type); // 次活動日/活動型態
			break;
		}
		String[] valueArray = new String[valueList.size()];
		valueList.toArray(valueArray);

		return valueArray;
	}

