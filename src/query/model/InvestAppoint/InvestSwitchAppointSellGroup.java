package query.model.InvestAppoint;

import java.util.ArrayList;
import java.util.List;

import query.model.AbstractModelObject;

public class InvestSwitchAppointSellGroup extends AbstractModelObject {
	private List<InvestSwitchSellAppoint03> investSwitchSellAppoint03s = new ArrayList<InvestSwitchSellAppoint03>();

	public InvestSwitchAppointSellGroup() {

	}

	public List<InvestSwitchSellAppoint03> getInvestSwitchSellAppoint03s() {
		return investSwitchSellAppoint03s;
	}

	public void addInvestAppoint03(InvestSwitchSellAppoint03 investAppoiint03) {
		List<InvestSwitchSellAppoint03> oldValue = investSwitchSellAppoint03s;
		investSwitchSellAppoint03s = new ArrayList<InvestSwitchSellAppoint03>(
				investSwitchSellAppoint03s);
		investSwitchSellAppoint03s.add(investAppoiint03);

		firePropertyChange("investSwitchSellAppoint03s", oldValue,
				investSwitchSellAppoint03s);
	}

	public void removeInvestAppoint03(InvestSwitchSellAppoint03 investAppiont03) {
		investSwitchSellAppoint03s.remove(investAppiont03);
	}

	public void removeAll() {
		List<InvestSwitchSellAppoint03> oldValue = investSwitchSellAppoint03s;
		investSwitchSellAppoint03s = new ArrayList<InvestSwitchSellAppoint03>(
				investSwitchSellAppoint03s);
		investSwitchSellAppoint03s.clear();
		firePropertyChange("investSwitchSellAppoint03s", oldValue,
				investSwitchSellAppoint03s);
	}

	public int getInvestAppoint03sCount() {
		return investSwitchSellAppoint03s.size();
	}

}
