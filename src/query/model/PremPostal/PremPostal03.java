package query.model.PremPostal;

import java.util.HashMap;

public class PremPostal03 {
	private String result_ind;
	private String transfer_bank;
	private String transfer_account;
	private String transfer_time;
	private String atm_code;
	private String atm_seq;
	private String atm_fee;

	public PremPostal03(HashMap<String, String> hsmp) {
		result_ind = (hsmp.get("r_result_ind") == null) ? "" : hsmp.get(
                "r_result_ind").trim();
		transfer_bank = (hsmp.get("r_transfer_bank") == null) ? "" : hsmp.get(
                "r_transfer_bank").trim();
		transfer_account = (hsmp.get("r_transfer_account") == null) ? "" : hsmp.get(
                "r_transfer_account").trim();
		transfer_time = (hsmp.get("r_transfer_time") == null) ? "" : hsmp.get(
                "r_transfer_time").trim();
		atm_code = (hsmp.get("r_atm_code") == null) ? "" : hsmp.get(
                "r_atm_code").trim();
		atm_seq = (hsmp.get("r_atm_seq") == null) ? "" : hsmp.get(
                "r_atm_seq").trim();
		atm_fee = (hsmp.get("r_atm_fee") == null) ? "" : hsmp.get(
                "r_atm_fee").trim();
	}
	
	public String getresult_ind() {
        return result_ind;
	}

	public void setresult_ind(String result_ind) {
        this.result_ind = result_ind;
	}

	public String gettransfer_bank() {
        return transfer_bank;
	}

	public void settransfer_bank(String transfer_bank) {
        this.transfer_bank = transfer_bank;
	}

	public String gettransfer_account() {
        return transfer_account;
	}

	public void settransfer_account(String transfer_account) {
        this.transfer_account = transfer_account;
	}

	public String gettransfer_time() {
        return transfer_time;
	}

	public void settransfer_time(String transfer_time) {
        this.transfer_time = transfer_time;
	}

	public String getatm_code() {
		return atm_code;
	}

	public void setatm_code(String atm_code) {
		this.atm_code = atm_code;
	}

	public String getatm_seq() {
		return atm_seq;
	}

	public void setatm_seq(String atm_seq) {
		this.atm_seq = atm_seq;
	}

	public String getatm_fee() {
		return atm_fee;
	}

	public void setatm_fee(String atm_fee) {
		this.atm_fee = atm_fee;
	}

}
