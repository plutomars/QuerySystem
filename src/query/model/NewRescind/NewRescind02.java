package query.model.NewRescind;

import java.util.HashMap;

public class NewRescind02 {
	private String r_result_ind;
	private String r_pocc_msg;

	public NewRescind02(HashMap<String, String> hsmp) {
		r_result_ind = (hsmp.get("r_result_ind") == null) ? "" : hsmp.get(
				"r_result_ind").trim();
		
		r_pocc_msg = (hsmp.get("r_pocc_msg") == null) ? "" : hsmp.get(
				"r_pocc_msg").trim();
	
	}

	public String getR_result_ind() {
		return r_result_ind;
	}

	public void setR_result_ind(String r_result_ind) {
		this.r_result_ind = r_result_ind;
	}

	public String getR_pocc_msg() {
		return r_pocc_msg;
	}

	public void setR_pocc_msg(String r_pocc_msg) {
		this.r_pocc_msg = r_pocc_msg;
	}

}
