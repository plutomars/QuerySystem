package query.model.NewDetail;

import java.util.HashMap;

/**
 * 新契約明細告知事項部份(JavaBean)
 * 
 * @author epf
 * 
 * @version 1.0<br>
 *          100/07/28<br>
 * 
 * @since 1.0
 */
public class NewDetailPoStatement {
	private String r_result_ind;
	private String r_client_ident;
	private String r_client_id;
	private String r_names;
	private String r_statement;
	private String r_receive_date;
	private String r_receive_no;
	private String r_policy_no;

	public NewDetailPoStatement(HashMap<String, String> hsmp) {
		r_result_ind = (hsmp.get("r_result_ind") == null) ? "" : hsmp.get(
				"r_result_ind").trim();
		r_client_ident = (hsmp.get("r_client_ident") == null) ? "" : hsmp.get(
				"r_client_ident").trim();
		r_client_id = (hsmp.get("r_client_id") == null) ? "" : hsmp.get(
				"r_client_id").trim();
		r_names = (hsmp.get("r_names") == null) ? "" : hsmp.get("r_names")
				.trim();
		r_statement = (hsmp.get("r_statement") == null) ? "" : hsmp.get(
				"r_statement").trim();
		r_receive_date = (hsmp.get("r_receive_date") == null) ? "" : hsmp.get(
				"r_receive_date").trim();
		r_receive_no = (hsmp.get("r_receive_no") == null) ? "" : hsmp.get(
				"r_receive_no").trim();
		r_policy_no = (hsmp.get("r_policy_no") == null) ? "" : hsmp.get(
				"r_policy_no").trim();
	}

	public String getR_result_ind() {
		return r_result_ind;
	}

	public void setR_result_ind(String r_result_ind) {
		this.r_result_ind = r_result_ind;
	}

	public String getR_client_ident() {
		return r_client_ident;
	}

	public void setR_client_ident(String r_client_ident) {
		this.r_client_ident = r_client_ident;
	}

	public String getR_client_id() {
		return r_client_id;
	}

	public void setR_client_id(String r_client_id) {
		this.r_client_id = r_client_id;
	}

	public String getR_names() {
		return r_names;
	}

	public void setR_names(String r_names) {
		this.r_names = r_names;
	}

	public String getR_statement() {
		return r_statement;
	}

	public void setR_statement(String r_statement) {
		this.r_statement = r_statement;
	}

	public String getR_receive_date() {
		return r_receive_date;
	}

	public void setR_receive_date(String r_receive_date) {
		this.r_receive_date = r_receive_date;
	}

	public String getR_receive_no() {
		return r_receive_no;
	}

	public void setR_receive_no(String r_receive_no) {
		this.r_receive_no = r_receive_no;
	}

	public String getR_policy_no() {
		return r_policy_no;
	}

	public void setR_policy_no(String r_policy_no) {
		this.r_policy_no = r_policy_no;
	}

}
